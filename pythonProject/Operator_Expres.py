# this program shows different operators and expression
num1 = 3
num2 = 5
num3 = num1 + num2  # num1 + num2 is the expression
print("The addition value is", num3)

num4 = num1 - num2
print("The subtraction value is", num4)

num5 = num1 * num2
print("The multiplication value", num5)

num6 = num1/num2
print("The division value is", num6)
num7 = num1//num2
print("The floor division value is", num7)
num8 = num1 % num2
print("The modulo value is", num8)
print("The leftshift decimal value is", num2 << num1)
print("The rightshift decimal value is",  num2 >> num1)
print("The bitwise AND value", num1 & num2)
print("The bitwise OR value", num1 | num2)
print("The bitwise XOR value", num1 ^ num2)
print("The bitwise complement value is", ~ num2)
